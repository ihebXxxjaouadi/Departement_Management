    private void tbldeptMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbldeptMouseClicked
        int CurrentRow = tbldept.getSelectedRow() ;
        DefaultTableModel model = (DefaultTableModel)tbldept.getModel() ;
        txtdepno.setText((String) model.getValueAt(CurrentRow , 0));
        txtdeptname.setText((String) model.getValueAt(CurrentRow , 1));
        txtLoc.setText((String) model.getValueAt(CurrentRow , 2));
        setButtonsStatus() ;
        

    }//GEN-LAST:event_tbldeptMouseClicked

