    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        int msgC = JOptionPane.showConfirmDialog(null, "do you want delete this employe") ;
        if (msgC == JOptionPane.YES_OPTION){
            dept.setDeptNo(Integer.parseInt(txtdepno.getText()));
            dept.delete();
            clearTexts(this) ;
            dept.getAllrows(tbldept);
            JOptionPane.showMessageDialog(null, "employe is deleted");
        }
        
    }//GEN-LAST:event_btndeleteActionPerformed

